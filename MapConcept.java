package com.collection.framework.continuation;

public class MapConcept 
{
	public static void main(String...args)
	{
		/*
		 * map is an interface
		 * it is a part of collection framework 
		 * it does not inherit any propertied in collection interface
		 * it has own methods
		 * hashmap ,linkedhashmap, treemap implementation of map interface
		 * 
		 * key-value association 
		 * each key associated with values
		 * map interface replacement of dictionary class which is an abstract class from java 1.0
		 * duplicate keys not allowed 
		 * duplicate values allowed
		 * key value pair  of the map are stored as Map.Entry
		 * order of elements hashmap dependent 
		 * linkedhashmap insertion order preserved 
		 * treemap order according to comparator
		 * 
		 * 
		 * 3 methods
		 * keySet()--set of keys
		 * values() -- set of values
		 * entrySet() -- set of key value mapping
		 * 
		 * 
		 * 	-------------------methods in map interface----------------
		 * 
		 * size()
		 * isEmpty()
		 * containsKey(object)
		 * containsValue(Object)
		 * get(object key)
		 * put(key,value)
		 * remove(key)
		 * putAll(map)
		 * clear()
		 * set<> keySet()
		 * Collection<> values()
		 * set<Map.Entry<Key,value> entrySet()
		 * equals() 
		 * hashCode() 	                                                                                                                                                                                             
         */
	}

}
